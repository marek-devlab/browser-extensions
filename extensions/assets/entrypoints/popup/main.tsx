import { StrictMode } from 'react';
import { createRoot } from 'react-dom/client';
import '@blur/ui/tokens.css';
import '@blur/ui/components.css';
import './style.css';
import { seedTheme } from '@blur/ui';
import { App } from './App';

// Synchronous theme seed before createRoot to avoid a light/dark flash (design
// §11.3). The async pref then confirms it on load.
seedTheme('blur-assets:theme');

const root = document.getElementById('root');
if (!root) throw new Error('Missing #root');
createRoot(root).render(
  <StrictMode>
    <App />
  </StrictMode>,
);
