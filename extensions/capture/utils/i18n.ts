const messages: Catalog<MsgKey> = { en, ru, et };

const translate = createTranslator<MsgKey>(messages);

/** Hook: a `t(key, vars?)` bound to the active React-tree locale. */
export function useT(): (key: MsgKey, vars?: Record<string, string | number>) => string {
  const locale = useLocale();
  return useCallback(
    (key: MsgKey, vars?: Record<string, string | number>) => translate(locale, key, vars),
    [locale],
  );
}

/** Non-React translation for a known locale (e.g. background, or a message
 *  listener registered outside render). */
export function tAt(
  locale: Locale,
  key: MsgKey,
  vars?: Record<string, string | number>,
): string {
  return translate(locale, key, vars);
}
