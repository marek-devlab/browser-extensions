const messages: Catalog<MsgKey> = { en, ru, et };

const translate = createTranslator<MsgKey>(messages);

/** Hook: a `t(key, vars?)` bound to the active locale (from LocaleProvider). */
export function useT() {
  const l = useLocale();
  return useCallback((k: MsgKey, v?: Record<string, string | number>) => translate(l, k, v), [l]);
}

/** Translate at an explicit locale — for non-React contexts (background) and
 *  pure utils that receive a locale/translator (convert, sanitize, env). */
export function tAt(locale: Locale, k: MsgKey, v?: Record<string, string | number>): string {
  return translate(locale, k, v);
}
